# include<stdio.h>

int main () {

           int a , b ;

           scanf("%d", &a ) ;

           scanf("%d", &b) ;

           printf("The difference of the given numbers : %d\n", a - b ) ;

           printf("The product of the given numbers : %d\n", a * b) ;

           printf("The quotient of the given numbers : %f\n",(float)a/b) ;

           printf("Modulus = %d\n", a % b) ;

           return 0 ; 

}

